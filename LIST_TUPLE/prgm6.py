# Write a Python program to print the 4th element from first and 4th element from the
# last in a tuple.
# tuple1 = ("a", "c", "a", "d", "e", "m", "y", 6, 9, 6)

tuple1 = ("a", "c", "a", "d", "e", "m", "y", 6, 9, 6)

fourth_from_first = tuple1[3]
fourth_from_last = tuple1[-4]

print("4th element from the first:", fourth_from_first)
print("4th element from the last:", fourth_from_last)
